#!/usr/bin/env bash
# kopia_common.sh
# ------------------------------------------------------------------------------
# Общий конфигурационный файл для скриптов резервного копирования
# и восстановления (kopia_backup.sh, kopia_restore_to.sh).
#
# Файл предназначен ТОЛЬКО для подключения через `source`, самостоятельно
# не запускается. Поэтому здесь нет разбора аргументов командной строки
# и нет безусловных `exit` вне функций — иначе при ошибке они завершили бы
# оболочку, из которой файл подключается.
# ------------------------------------------------------------------------------

set -uo pipefail



# ---- Версия проекта скриптов -------------------------------------------------
# Используется ключом -v/--version в kopia_backup.sh и kopia_restore_to.sh.
# Меняйте при значимых изменениях логики скриптов.
APP_VERSION="1.0.0 (2026-07-01)"
COPYRIGHT="Copyright (C) 2026 Ariv <ariv@meta.ua> | https://github.com/arivm7 | RI-Network, Kiev, UK"
LAST_CHANGES="\
v1.0.0 (2026-07-01): Базовый функционал резервного копирования и восстановления
"


SCRIPT_NAME=$(basename "${BASH_SOURCE[0]}") # Полное имя вложенного скрипта, включая расширение

# ---- Домашний каталог пользователя, от чьего имени реально запущен скрипт ---
# После `sudo` (см. require_root) $HOME может смениться на /root.
# SUDO_USER всегда содержит исходное имя пользователя — берём его домашний
# каталог через getent, чтобы пути ниже не зависели от того, произошла
# эскалация прав или нет.
if [[ -n "${SUDO_USER:-}" ]]; then
    REAL_HOME="$(getent passwd "$SUDO_USER" | cut -d: -f6)"
else
    REAL_HOME="$HOME"
fi

# ---- Каталог с пользовательскими конфигами скриптов -------------------------
CONFIG_DIRNAME="kopia_backup"
CONFIG_PATH="${REAL_HOME}/.config/${CONFIG_DIRNAME}"
CONFIG_FILE="${CONFIG_PATH}/${FILE_NAME}.conf"



COLOR_OK="\e[0;32m"         # Терминальный цвет для вывода успешного статуса
COLOR_USAGE="\e[1;32m"      # Терминальный цвет для вывода переменной статуса
COLOR_ERROR="\e[0;31m"      # Терминальный цвет для вывода ошибок
COLOR_INFO="\e[0;34m"       # Терминальный цвет для вывода информации (об ошибке или причине выхода)
COLOR_FILENAME="\e[1;36m"   # Терминальный цвет для вывода имён файлов
COLOR_OFF="\e[0m"           # Терминальный цвет для сброса цвета

APP_AWK="awk"

LOG_PREFIX="BACKUPER"       # Используется для префикса в системном логе



##
##  ============================================================================
##  [CONFIG START] Начало секции конфига
##



#
# ---- Параметры репозитория kopia --------------------------------------------
# REPO_PATH    — каталог, где физически хранятся данные репозитория kopia.
REPO_PATH="${REAL_HOME}/Backups/kopia_backup/$(hostname --short)"



# KOPIA_CONFIG — путь к файлу конфигурации подключения kopia.
KOPIA_CONFIG="${CONFIG_PATH}/repository.config"



#
# Файл с паролем репозитория. Используется для неинтерактивного подключения
# (cron, автоматизация), чтобы kopia не зависала на запросе пароля.
# Права доступа должны быть 600.
KOPIA_PASSWORD_FILE="${CONFIG_PATH}/.kopia_pass"



#
# ---- Целевая папка восстановления по умолчанию ------------------------------
# Каждый запуск без явного пути создаёт новую подпапку с меткой времени.
RESTORE_ROOT_DEFAULT="${REAL_HOME}/Backups/kopia_restore/$(date +%Y%m%d_%H%M%S)"



#
# ---- Список каталогов и файлов, входящих в резервную копию ------------------
# Обратите внимание: список содержит и каталоги, и отдельные файлы —
# kopia одинаково умеет бэкапить оба типа.
SOURCE_PATHS=(
    "/etc"
    "/var/www"
    "/var/spool/cron"
    "/home"
    "/root"
)



#
# ---- Исключения из резервной копии ------------------------------------------
# Обратите внимание: список содержит и каталоги, и отдельные файлы —
# kopia одинаково умеет исключать оба типа.
# Создаёте файл .kopiaignore прямо внутри каталога, который бэкапите:
# Пример содержимого файла /var/www/.kopiaignore
# ---- cut ----
# cache/
# tmp/
# *.log
# node_modules/
# uploads/temp/
# ---- end cut ----
# 
# Синтаксис правил такой же, как в .gitignore:
# cache/ — исключить папку cache на любом уровне вложенности
# /cache/ — исключить cache только в корне указанного пути (не глубже)
# *.log — исключить все файлы с расширением .log
# !important.log — исключение из исключения (не игнорировать конкретный файл, даже если он попадает под *.log)
#



##
##  [CONFIG END] Конец секции конфига
##  ----------------------------------------------------------------------------
##



#
#  Обёртка для logger -p info
#
log_info() {
    logger -p info -t "${LOG_PREFIX}" "$*"
}



#
#  Обёртка для logger -p error
#
log_error() {
    logger -p error -t "${LOG_PREFIX}" "$*"
}



#
# Вывод строки и выход из скрипта
# $1 -- сообщение
# $2 -- код ошибки. По умолчанию "1"
#
exit_with_msg() {
    local msg="${1:?Строка не передана или пуста. Смотреть вызывающую функцию.}"
    local num="${2:-1}"
    case "${num}" in
    1)
        log_error "ERR: ${msg}"
        msg="[${COLOR_ERROR}Ошибка${COLOR_OFF}] ${msg}"
        ;;
    2)
        log_error "ERR: ${msg}"
        msg="[${COLOR_ERROR}Ошибка${COLOR_OFF}] ${msg}"
        msg="${msg}\nПодсказка по использованию: ${COLOR_USAGE}${APP_NAME} --usage|-u${COLOR_OFF}"
        ;;
    0)
        log_info "OK: ${msg}"
        msg="[${COLOR_OK}Ok${COLOR_OFF}] ${msg}"
        ;;
    *)
        log_info "${msg}"
        msg="[${COLOR_INFO}i${COLOR_OFF}] ${msg}"
        ;;
    esac
    echo -e "${msg}"
    exit "$num"
}



#
#  Записывает в конфиг файл фрагмент этого же скрипта между маркерами
#  CONFIG START и CONFIG END (см. регулярное выражение ниже)
#  Используемые глобальные переменные: BASH_SOURCE[0] и CONFIG_FILE
#
save_config_file()
{
    mkdir -p "${CONFIG_PATH}"
    echo -e "Инициализация конфиг-файла '${COLOR_FILENAME}${CONFIG_FILE}${COLOR_OFF}'"
    if ! command -v "${APP_AWK}" >/dev/null 2>&1; then
        exit_with_msg "Нет приложения ${COLOR_FILENAME}${APP_AWK}${COLOR_OFF}." 1
    fi
    # Извлечь фрагмент между маркерами CONFIG START и CONFIG END из самого скрипта
    "${APP_AWK}" '/\[[[:space:]]*CONFIG START[[:space:]]*\]/,/\[[[:space:]]*CONFIG END[[:space:]]*\]/' "${BASH_SOURCE[0]}" > "${CONFIG_FILE}"

    exit_with_msg "Конфиг-файл '${COLOR_FILENAME}${CONFIG_FILE}${COLOR_OFF}' создан. Отредактируйте его при необходимости и запустите скрипт заново." 0
}



#
#  Чтение конфигурационного файла.
#  Если его нет, то создание.
#
read_config_file()
{
    if [ -f "${CONFIG_FILE}" ]; then
        # shellcheck disable=SC1090
        source "${CONFIG_FILE}"
    else
        save_config_file
    fi
}



# ---- Требуемые для работы скриптов программы --------------------------------
# Формат каждого элемента массива: "имя_команды:apt_пакет"
#
#   - Если после двоеточия указано имя пакета — при отсутствии команды
#     скрипт предложит установить пакет автоматически через apt.
#   - Если после двоеточия ПУСТО — программа должна быть установлена
#     вручную (например, kopia в стандартных репозиториях apt отсутствует
#     и ставится по официальной инструкции с сайта kopia.io). В этом
#     случае скрипт не пытается ничего ставить сам, а сразу сообщает
#     об ошибке и завершает работу.
#
# Добавляйте сюда новые зависимости по мере необходимости, ничего в самих
# функциях проверки менять не нужно.
REQUIRED_PACKAGES=(
    "kopia:"   # kopia — устанавливается вручную, apt-пакета нет
    "jq:jq"    # jq — используется для надёжного разбора JSON-вывода kopia
)



# ------------------------------------------------------------------------------
# check_required_packages
#
#   Проверяет наличие всех программ, перечисленных в REQUIRED_PACKAGES.
#
#   Поведение:
#     1. Программы без apt-пакета (ручная установка) — если отсутствуют,
#        сразу выводится ошибка со списком и скрипт завершается (exit 1).
#        Автоматическая установка для них не предлагается и не выполняется.
#     2. Программы с apt-пакетом — если отсутствуют, пользователю
#        задаётся вопрос об установке через apt.
#          - При согласии (y/Y/yes/Yes) — выполняется
#            `apt-get update && apt-get install -y <пакеты>`.
#            Если установка завершилась с ошибкой — скрипт завершается (exit 1).
#          - При отказе — скрипт завершается (exit 1), чтобы не продолжать
#            работу без необходимых зависимостей.
#
#   Функцию нужно вызывать после `source kopia_common.sh`, но до любых
#   команд, использующих внешние утилиты (kopia, jq и т.д.).
# ------------------------------------------------------------------------------
check_required_packages() {
    local entry bin pkg
    local missing_manual=()
    local missing_installable=()

    for entry in "${REQUIRED_PACKAGES[@]}"; do
        bin="${entry%%:*}"   # часть до двоеточия — имя команды
        pkg="${entry#*:}"    # часть после двоеточия — apt-пакет (может быть пустой)

        if command -v "$bin" &>/dev/null; then
            continue
        fi

        if [[ -n "$pkg" ]]; then
            missing_installable+=("$pkg")
        else
            missing_manual+=("$bin")
        fi
    done

    # ---- Программы, которые нельзя поставить автоматически ------------------
    if (( ${#missing_manual[@]} > 0 )); then
        echo "ОШИБКА: не найдены следующие программы, требующие ручной установки:" >&2
        printf '  - %s\n' "${missing_manual[@]}" >&2
        exit_with_msg "Установите их самостоятельно (см. документацию) и запустите скрипт заново." 1
    fi

    # ---- Программы, доступные через apt --------------------------------------
    if (( ${#missing_installable[@]} > 0 )); then
        echo "Не найдены следующие необходимые пакеты: ${missing_installable[*]}"
        read -r -p "Установить их через apt сейчас? (y/N): " ANSWER

        if [[ "$ANSWER" =~ ^[Yy]([Ee][Ss])?$ ]]; then
            echo "Устанавливаем: ${missing_installable[*]} ..."
            if ! apt-get update || ! apt-get install -y "${missing_installable[@]}"; then
                exit_with_msg "ОШИБКА: установка пакетов не удалась." 1
            fi
        else
            exit_with_msg "Установка отменена пользователем. Выполнение скрипта прекращено." 1
        fi
    fi
}



# ------------------------------------------------------------------------------
# check_kopia_password_file
#
#   Проверяет, что файл с паролем репозитория (KOPIA_PASSWORD_FILE):
#     1. существует;
#     2. имеет права доступа 600 (чтение/запись только владельцем).
#
#   При невыполнении любого из условий выводит понятную ошибку с
#   инструкцией по исправлению и завершает скрипт (exit 1).
#
#   Вызывать нужно при каждом запуске скрипта, до попытки подключения
#   к репозиторию — иначе `kopia repository connect` в неинтерактивном
#   режиме (cron) зависнет на запросе пароля.
# ------------------------------------------------------------------------------
check_kopia_password_file() {
    if [[ ! -f "${KOPIA_PASSWORD_FILE}" ]]; then

MSG="$(cat <<EOF
ОШИБКА: файл с паролем репозитория не найден: ${KOPIA_PASSWORD_FILE}
Создайте его, например:
    mkdir -p "$(dirname "${KOPIA_PASSWORD_FILE}")"
    echo 'ваш_пароль_репозитория' > ${KOPIA_PASSWORD_FILE}
    chmod 600 ${KOPIA_PASSWORD_FILE}
EOF
)"
        exit_with_msg "${MSG}" 1
    fi

    local perms
    perms="$(stat -c '%a' "${KOPIA_PASSWORD_FILE}" 2>/dev/null)"

    if [[ "$perms" != "600" ]]; then
        echo "ОШИБКА: права доступа файла ${KOPIA_PASSWORD_FILE} должны быть 600 (сейчас: ${perms:-неизвестно})." >&2
        exit_with_msg "Исправьте: chmod 600 ${KOPIA_PASSWORD_FILE}" 1
    fi
}



# ------------------------------------------------------------------------------
# require_root
#
#   Гарантирует, что дальнейший код выполняется от root.
#
#   Если скрипт запущен обычным пользователем (EUID != 0), функция
#   перезапускает САМА СЕБЯ через `sudo`, передавая все исходные аргументы
#   командной строки без изменений (включая -h/--help/-v и т.д., хотя
#   в реальности эту функцию нужно вызывать уже ПОСЛЕ обработки
#   информационных ключей — им root не нужен).
#
#   Использует `exec`, поэтому текущий процесс полностью замещается новым
#   (sudo ...), а не порождает дочерний процесс — после успешного
#   перезапуска функция никогда не возвращает управление исходному
#   непривилегированному процессу.
#
#   Если у пользователя нет прав sudo или он отменит ввод пароля,
#   `sudo` сам завершится с ошибкой и ненулевым кодом — в этом случае
#   `exec` не срабатывает, выполнение просто продолжится и упадёт на
#   `exit 1` ниже.
#
#   Использование: require_root "$@"   (передавайте оригинальные $@)
# ------------------------------------------------------------------------------
require_root() {
    if [[ "$(id -u)" -eq 0 ]]; then
        return 0
    fi

    if ! command -v sudo &>/dev/null; then
        exit_with_msg "Скрипт запущен не от root, а команда sudo не найдена. Запустите скрипт от root." 1
    fi

    echo "Скрипт запущен не от root — перезапуск через sudo..."
    exec sudo -- "$0" "$@"

    # Сюда код доходит, только если exec не смог заменить процесс
    # (например, sudo не установлен корректно) — на всякий случай
    # завершаем работу с ошибкой, а не продолжаем без root.
    exit_with_msg "Не удалось перезапустить скрипт через sudo." 1
}

