#!/usr/bin/env bash
# kopia_backup.sh
# ------------------------------------------------------------------------------
# Скрипт регулярного резервного копирования каталогов и файлов из списка
# SOURCE_PATHS (см. kopia_common.sh) в репозиторий kopia.
#
# При первом запуске подключается к существующему репозиторию или создаёт
# новый (если он ещё не был инициализирован). При каждом запуске создаёт
# снапшот для каждого пути из SOURCE_PATHS, применяет политику хранения
# и выполняет лёгкое обслуживание репозитория.
#
# Требует root-доступ (часть путей из SOURCE_PATHS доступна на чтение
# только root: /etc, /var/spool/cron, /var/www и т.д.). Если скрипт запущен
# обычным пользователем, он сам перезапускается через sudo (require_root).
# ------------------------------------------------------------------------------

set -uo pipefail



APP_TITLE="Скрипт создания резервной копии. Оёртка для Kopia - Fast And Secure Open-Source Backup"

APP_NAME="$(basename "$0")"
APP_DIR="$(cd "$(dirname "$0")" && pwd)"    # Папка размещения скрипта
FILE_NAME="${APP_NAME%.*}"                  # Убираем расширение (если есть)




# ---- Справка и использование
print_usage() {
    echo "Использование: ${APP_NAME} [-h|--help] [--usage] [-v|--version]"
}

print_help() {
    print_usage
cat <<EOF

${APP_TITLE}
${APP_NAME} — резервное копирование каталогов и файлов через kopia.

Ключи:
  -h, --help       показать эту справку и выйти
  -u, --usage      показать краткую строку использования и выйти
  -v, --version    показать версию скрипта и выйти

Описание:
  Скрипт подключается (или, при первом запуске, создаёт) к репозиторию
  kopia, указанному в kopia_common.sh (переменная REPO_PATH), затем
  создаёт снапшоты для всех путей из массива SOURCE_PATHS, применяет
  к ним политику хранения (30 суточных / 12 недельных / 12 месячных
  снапшотов) и выполняет лёгкое обслуживание репозитория.

  Полное обслуживание (kopia maintenance run --full) в этот скрипт
  не входит намеренно — его рекомендуется запускать отдельным
  cron-заданием раз в неделю.

  Часть путей из SOURCE_PATHS доступна на чтение только root (/etc,
  /var/spool/cron, /var/www и т.д.). Если скрипт запущен обычным
  пользователем, он сам перезапускается через sudo (см. require_root
  в kopia_common.sh) — вызывать sudo вручную не обязательно.

Файлы:
  kopia_common.sh   — общие настройки: пути репозитория, список
                      источников резервного копирования, список
                      требуемых программ.
Версия: ${APP_VERSION}
${COPYRIGHT}
EOF
}



# ---- Подключаем общий конфиг -------------------------------------------------
source "${APP_DIR}/kopia_common.sh"



# ---- Разбор аргументов командной строки -------------------------------------
# Обрабатываем только первый аргумент — этого достаточно для стандартных
# информационных ключей, других аргументов скрипт не принимает.
case "${1:-}" in
    -h|--help)
        print_help
        exit 0
        ;;
    -u|--usage)
        print_usage
        exit 0
        ;;
    -v|--version)
        # print_version() определена в kopia_common.sh
        print_version
        exit 0
        ;;
esac



echo ""
echo ""
echo ""
echo "==================================="
date



# ---- Чтение/создание пользовательского конфига -------------------------------
# Выполняется ДО эскалации прав, поэтому файл создаётся и остаётся
# собственностью обычного пользователя — редактировать его можно без sudo.
read_config_file



echo "Путь репозитория     : ${REPO_PATH}"
echo "Конфигурация обёртки : ${CONFIG_PATH}"
echo "Конфигурация kopia   : ${KOPIA_CONFIG}"
echo ""



# ---- Самоповышение прав ------------------------------------------------------
# Если скрипт запущен обычным пользователем — перезапускаем его через sudo.
# Все дальнейшие операции (чтение /etc, /var/www и т.д., создание снапшотов)
# выполняются уже от root. Справка/версия и чтение конфига выше отработали
# без sudo.
require_root "$@"



# ---- 0. Проверка необходимых программ ---------------------------------------
# При отсутствии программ, доступных через apt, будет предложена установка.
# При отказе или при отсутствии программ, требующих ручной установки,
# скрипт завершится с кодом 1 (см. kopia_common.sh).
check_required_packages



# ---- 0.1. Проверка файла с паролем репозитория -------------------------------
# Проверяется при каждом запуске: файл должен существовать, принадлежать
# root и иметь права 600. При несоответствии — ошибка и exit 1.
check_kopia_password_file



# ---- 1. Проверка и подключение / создание репозитория -----------------------
# Сначала смотрим, не подключён ли репозиторий уже (обычная ситуация при
# повторных запусках — тогда пароль не запрашивается благодаря сохранённой
# сессии в KOPIA_CONFIG).
if kopia repository status --config-file "$KOPIA_CONFIG" &>/dev/null; then
    echo "Репозиторий уже подключён."
else
    # Не подключён — проверяем, существует ли репозиторий физически на диске
    if [[ -f "$REPO_PATH/kopia.repository" ]]; then
        echo "Репозиторий найден, подключаемся..."
        # Пароль репозитория хранится в защищённом файле (chmod 600, root:root),
        # чтобы неинтерактивные запуски (cron) не зависали на запросе пароля.
        # Наличие и права файла уже проверены выше функцией check_kopia_password_file.
        KOPIA_PASSWORD="$(cat "$KOPIA_PASSWORD_FILE")"
        export KOPIA_PASSWORD
        kopia repository connect filesystem \
            --path "$REPO_PATH" \
            --config-file "$KOPIA_CONFIG" || {
                exit_with_msg "ОШИБКА: не удалось подключиться к репозиторию" 1
            }
    else
        echo "Репозиторий не найден, создаём новый..."
        mkdir -p "$REPO_PATH"
        kopia repository create filesystem \
            --path "$REPO_PATH" \
            --config-file "$KOPIA_CONFIG" || {
                exit_with_msg "ОШИБКА: не удалось создать репозиторий" 1
            }
    fi
fi



# ---- 2. Снапшоты и политика хранения для каждого пути ------------------------
FAILED=()

for DIR in "${SOURCE_PATHS[@]}"; do

    if [[ -d "$DIR" ]]; then
        echo "Бэкап каталога: $DIR"
    elif [[ -f "$DIR" ]]; then
        echo "Бэкап файла: $DIR"
    else
        echo "ПРОПУСК: $DIR не найден" >&2
        continue
    fi

    if ! kopia snapshot create "$DIR" --config-file "$KOPIA_CONFIG"; then
        echo "ОШИБКА: снапшот для $DIR не создан" >&2
        FAILED+=("$DIR")
        continue
    fi

    # Политика хранения: 30 ежедневных, 12 еженедельных, 12 ежемесячных снапшотов
    kopia policy set "$DIR" --config-file "$KOPIA_CONFIG" --keep-daily 30 --keep-weekly 12 --keep-monthly 12
done



# ---- 3. Лёгкое обслуживание репозитория --------------------------------------
# Полное обслуживание (--full) сюда намеренно не входит — запускайте его
# отдельным cron-заданием раз в неделю: kopia maintenance run --full
kopia maintenance run --config-file "$KOPIA_CONFIG"



# ---- 4. Итог ------------------------------------------------------------------
if (( ${#FAILED[@]} > 0 )); then
    exit_with_msg "Бэкап завершён с ошибками для: ${FAILED[*]}" 1
fi

exit 0
